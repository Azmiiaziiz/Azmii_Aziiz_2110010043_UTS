    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // Mengambil model dari tabel (DefaultTableModel) yang ada di objek tbTugas
        DefaultTableModel model = (DefaultTableModel) tbTugas.getModel();
        
        // Memeriksa apakah input nama, tanggal, atau tugas kosong
        if (tfNama.getText().trim().equals("") || tfTanggal.getText().trim().equals("") || taTugas.getText().trim().equals("")) {
            // Menampilkan pesan kesalahan jika ada input yang kurang lengkap
            JOptionPane.showMessageDialog(null, "Tugas yang anda isi kurang lengkap \n silahkan isi dengan lengkap");
        } else {
            // Jika input lengkap, menambahkan baris baru ke model tabel dengan data dari input
            model.addRow(new Object[]{tfNama.getText(), tfTanggal.getText(), taTugas.getText(), cbStatus.getSelectedItem()});
            // Menampilkan pesan sukses jika tugas berhasil diinput
            JOptionPane.showMessageDialog(null, "tugas anda berhasil di input");
        }
    }//GEN-LAST:event_btnTambahActionPerformed

