    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // Mengambil model dari tabel (DefaultTableModel) yang ada di objek tbTugas
        DefaultTableModel model = (DefaultTableModel) tbTugas.getModel();
        
        // Memeriksa apakah tidak ada baris yang dipilih di tabel
        if (tbTugas.getSelectedRow() == -1) {
            // Memeriksa apakah baris pertama dipilih (0)
            if (tbTugas.getSelectedRow() == 0) {
                // Menampilkan pesan kesalahan jika baris pertama yang dipilih
                JOptionPane.showMessageDialog(null, "anda belum memilih tugas ");
            } else {
                // Menampilkan pesan kesalahan jika tidak ada baris yang dipilih
                JOptionPane.showMessageDialog(null, "Silahkan Pilih Tugas");
            }
        } else {
            // Jika ada baris yang dipilih, mengisi nilai input dengan nilai dari baris yang dipilih
            tfNama.setText(model.getValueAt(tbTugas.getSelectedRow(), 0).toString());
            tfTanggal.setText(model.getValueAt(tbTugas.getSelectedRow(), 1).toString());
            cbStatus.setSelectedItem(model.getValueAt(tbTugas.getSelectedRow(), 3).toString());
            taTugas.setText(model.getValueAt(tbTugas.getSelectedRow(), 2).toString());
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapustblActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapustblActionPerformed
        // Mengambil model dari tabel (DefaultTableModel) yang ada di objek tbTugas
        DefaultTableModel model = (DefaultTableModel) tbTugas.getModel();
        
        // Memeriksa apakah tidak ada baris yang dipilih di tabel
        if (tbTugas.getSelectedRow() == -1) {
            // Memeriksa apakah baris pertama (indeks 0) yang dipilih
            if (tbTugas.getSelectedRow() == 0) {
                // Menampilkan pesan kesalahan jika baris pertama yang dipilih
                JOptionPane.showMessageDialog(null, "Pilih tugas yang ingin di hapus");
            } else {
                // Menampilkan pesan kesalahan jika tidak ada baris yang dipilih
                JOptionPane.showMessageDialog(null, "Pilih tugas yang di hapus");
            }
        } else {
            // Menghapus baris yang dipilih dari model tabel
            model.removeRow(tbTugas.getSelectedRow());
            
            // Menampilkan pesan sukses bahwa data tugas berhasil dihapus
            JOptionPane.showMessageDialog(null, "Data tugas Berhasil di hapus");
        }
        // Mengosongkan nilai pada input setelah tugas dihapus
        tfNama.setText("");
        tfTanggal.setText("");
        cbStatus.setSelectedItem("sudah di kerjakan");
        taTugas.setText("");
        
        // Memfokuskan kursor ke JTextField dengan variabel tfNama
        tfNama.requestFocus();
    }//GEN-LAST:event_btnHapustblActionPerformed

