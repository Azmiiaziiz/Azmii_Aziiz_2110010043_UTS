    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // Mengambil model dari tabel (DefaultTableModel) yang ada di objek tbTugas
        DefaultTableModel model = (DefaultTableModel) tbTugas.getModel();
        
        // Memeriksa apakah tidak ada baris yang dipilih di tabel
        if (tbTugas.getSelectedRow() == 1) {
            // Memeriksa apakah tabel tidak memiliki baris
            if (tbTugas.getRowCount() == 0) {
                // Menampilkan pesan jika tidak ada baris untuk dipilih
                JOptionPane.showMessageDialog(null, "Pilih tugas yang mau pertama");
            } else {
                // Menampilkan pesan jika tidak ada baris yang dipilih
                JOptionPane.showMessageDialog(null, "pilih tugas yang mau di hapus");
            }
        } else {
            // Jika ada baris yang dipilih, mengubah nilai pada model tabel dengan nilai dari input
            model.setValueAt(tfNama.getText(), tbTugas.getSelectedRow(), 0);
            model.setValueAt(tfTanggal.getText(), tbTugas.getSelectedRow(), 1);
            model.setValueAt(cbStatus.getSelectedItem(), tbTugas.getSelectedRow(), 3);
            model.setValueAt(taTugas.getText(), tbTugas.getSelectedRow(), 2);
            
            
            // Mengaktifkan tombol tambah (btnTambah)
            btnTambah.setEnabled(true);
            
            // Menampilkan pesan sukses bahwa tugas sudah diubah
            JOptionPane.showMessageDialog(null, "tugas sudah di ubah");
            
            // Mengosongkan nilai pada input setelah tugas diubah
            tfNama.setText("");
            tfTanggal.setText("");
            cbStatus.setSelectedItem("Sudah di Kerjakan ");
            taTugas.setText("");

        }
    }//GEN-LAST:event_btnSimpanActionPerformed

