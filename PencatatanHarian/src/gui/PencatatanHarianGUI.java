    private void btnExportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportActionPerformed
        try {
             // Membuka file "ExportData.txt" untuk ditulis menggunakan BufferedWriter
            BufferedWriter bw = new BufferedWriter(new FileWriter("ExportData.txt"));
            
            // Iterasi melalui setiap baris (rows) pada tabel (tbTugas)
            for (int i = 0; i < tbTugas.getRowCount(); i++) { //membaca rows table
                for (int j = 0; j < tbTugas.getColumnCount(); j++) { //membaca column table
                    bw.write(tbTugas.getValueAt(i, j).toString() + " ");
                }
                // Menambahkan baris baru setelah semua kolom pada baris tertentu telah ditulis
                bw.newLine();
            }
            
            // Menampilkan pesan sukses bahwa data berhasil diexport
            JOptionPane.showMessageDialog(null, "Data Berhasil Export");
            
            // Menutup BufferedWriter setelah selesai menulis
            bw.close();
        } catch (Exception e) {
            // Menangani exception jika terjadi kesalahan
            System.err.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnExportActionPerformed

